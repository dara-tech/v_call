export function latLngToVector3(lat: number, lng: number, radius: number) {
  const phi = (90 - lat) * (Math.PI / 180);
  const theta = (lng + 180) * (Math.PI / 180);
  return {
    x: -radius * Math.sin(phi) * Math.cos(theta),
    y: radius * Math.cos(phi),
    z: radius * Math.sin(phi) * Math.sin(theta),
  };
}

/** Ease-in-out cubic — smooth start and end for fly animations. */
export function easeInOutCubic(t: number): number {
  return t < 0.5 ? 4 * t * t * t : 1 - (-2 * t + 2) ** 3 / 2;
}

export function vector3ToLatLng(x: number, y: number, z: number) {
  const r = Math.sqrt(x * x + y * y + z * z) || 1;
  const lat = 90 - (Math.acos(y / r) * 180) / Math.PI;
  const lng = ((Math.atan2(z, -x) * 180) / Math.PI) - 180;
  return { lat, lng };
}
